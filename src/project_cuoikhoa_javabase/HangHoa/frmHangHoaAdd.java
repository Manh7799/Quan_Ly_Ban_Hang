    private void hienThiDanhSachQuocGia() {
        QuocGiaBusiness chuyenKhoaBusiness = new QuocGiaBusiness();

        //Lấy danh sách
        List<QuocGia> lstQuocGia = chuyenKhoaBusiness.layDanhSach();

        DefaultComboBoxModel model = new DefaultComboBoxModel();

        //Đuyệt để thêm vào model
        for (QuocGia objQuocGia : lstQuocGia) {
            model.addElement(objQuocGia);
        }

        //Render lại thông tin hiển thị lên combobox
        cboXuatXu.setRenderer(new Render());

        cboXuatXu.setModel(model);
    }

    private void hienThiChiTiet() {
        //Lay thong tin
        HangHoa objHangHoa = DataProvider.getHangHoaBus().layChiTiet(maHangHoa);

        if (objHangHoa != null) {
            //Hien thi len giao dien
            txtMaHang.setText(maHangHoa);
            txtTenHang.setText(objHangHoa.getTenHang());
            txtMoTa.setText(objHangHoa.getMoTa());
            txtGiaBan.setText(objHangHoa.getGiaBan());

            QuocGiaBusiness quocGiaBusiness = new QuocGiaBusiness();

            QuocGia objQuocGia = quocGiaBusiness.layChiTietKhoa(objHangHoa.getXuatXuId());

            if (objQuocGia != null) {
                //Hiển thị quốc qia của sản phẩm được chọn trên combobox
                cboXuatXu.getModel().setSelectedItem(objQuocGia);
            }
        }
    }

