    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        String maHang = "", tenHang = "", moTa = "", xuatXu = "", giaBanText = "";

        maHang = txtMaHang.getText();
        tenHang = txtTenHang.getText();
        moTa = txtMoTa.getText();
        giaBanText = txtGiaBan.getText();

        QuocGia objQuocGia = (QuocGia) cboXuatXu.getSelectedItem();
        if (objQuocGia != null) {
            xuatXu = objQuocGia.getMaQuocGia();
        }

        if (maHang.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Không được để trống mã hàng !");
            txtMaHang.requestFocus();
            return;
        }

        if (tenHang.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Không được để trống tên hàng !");
            txtTenHang.requestFocus();
            return;
        }

        if (giaBanText.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Không được để trống giá bán!");
            txtGiaBan.requestFocus();
            return;
        }

        int giaBan;

        try {
            giaBan = Integer.parseInt(giaBanText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Giá bán phải là một số nguyên!");
            txtGiaBan.requestFocus();
            return;
        }

        //Khởi tạo đối tượng
        HangHoa objHangHoa = new HangHoa();

        //gan gia tri
        objHangHoa.setMaHang(maHang);
        objHangHoa.setTenHang(tenHang);
        objHangHoa.setMoTa(moTa);
        objHangHoa.setGiaBan(giaBanText);
        objHangHoa.setXuatXuId(xuatXu);

        boolean ketQua = false;

        HangHoaBusiness business = new HangHoaBusiness();
        if (!maHangHoa.isEmpty()) {
            ketQua = business.suaDoi(objHangHoa);
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin hàng hóa thành công");

            //Reload lại danh sách
            frmHangHoa.hienThiDanhSachHangHoa();
        } else {
            ketQua = business.themMoi(objHangHoa);
            JOptionPane.showMessageDialog(rootPane, "Thêm mới thông tin hàng hóa thành công");

            //Reload lại danh sách
            frmHangHoa.hienThiDanhSachHangHoa();
        }

    }//GEN-LAST:event_btnCapNhatActionPerformed

